class TypeCasting{
	public static void main(String [] args){
		short a = 10;
		int b = 12;
		int c = (int)a;
		float f = 12.3f;
		double d = f;
		int g = 140;
		short h = (short) g;
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(f);
		System.out.println(d);
		System.out.println(h);
	}
}